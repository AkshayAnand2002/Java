//CALCULATE (V^2-U^2)/2AS IN JAVA.
public class java11 {
    public static void main(String[] args) {
        float a = 5;
        float S = 6;
        float v = 7;
        float u = 8;
        float x = (v*v)-(u*u);
        float y = (2*a*S);
        float z = x/y;
        System.out.println(z);
    }
}
